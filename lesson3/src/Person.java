public class Person
{

  //attributes
  private String name;
  private String birthday;

  //methods

  //set the methods
  public void setName(String personName){
    name = personName;
  }
  public void setBirthday(String personBirthday){
    birthday = personBirthday;
  }

  //get the methods
  public String getName(){
    return name;
  }

  public String getBirthday(){
    return birthday;
  }

}
